a = 100
print(type(a))  # 整型 int

b = 3.14
print(type(b))  # 浮点型 float

c = True
print(type(c))  # 布尔型 bool

d = ''' "87'6'4 '''  # 字符串 str
print(d)
print(type(d))

e = [1, 6, 3, 9, 0]  # 列表list
print(type(e))

f = (1, 6, 3, 9, 0)  # 元组 tuple
print(type(f))

g = {1: 'hello', 'xiaoming': 18}  # 字典 dict
print(type(g))

h = {10, 20, 'python'}  # 集合 set
print(type(h))



